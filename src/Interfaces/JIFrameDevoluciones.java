    //Insercion a la tabla detalleprestamo
    public void EjecutarDevolcion() {
        //Variables
        String isbnx = "";
        int idPrestamo = 0;
        int codigoDev = 0;
        int cantaDevuelta = 0;
        int noFilas = tblDetalleDevolucion.getRowCount();

        //Validamos que los campos contengan los datos necesarios
        if (txtClaveUsuario.getText().equals("") || txtNombre.getText().equals("") || noFilas < 1) {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos,o asegurese de \n realizar una devolucion", "Msj.", JOptionPane.WARNING_MESSAGE);

        } else {
            //Ejecutamos la inserción en la tabla Devolucion
            objDevolucion.realizarDevolucion(txtCodigoDevolucion.getText(), txtFecha.getText(), txtClaveUsuario.getText());

            //Contador para recorrer tantas filas encontradas en la tblDetalleDevolucion
            for (int fila = 0; fila < noFilas; fila++) {
                //Capturamos los valores 
                idPrestamo = Integer.parseInt(tblDetalleDevolucion.getValueAt(fila, 0).toString());
                codigoDev = Integer.parseInt(tblDetalleDevolucion.getValueAt(fila, 1).toString());
                isbnx = tblDetalleDevolucion.getValueAt(fila, 2).toString();
                cantaDevuelta = Integer.parseInt(tblDetalleDevolucion.getValueAt(fila, 4).toString());
                
                //Ejecutamos la inserción en la tabla detalleDevolucion
                objDevolucion.DetalleDevolucion(idPrestamo, codigoDev, isbnx, cantaDevuelta);
                //Ejecutamos estas lineas para actualizar la cantidad prestada
                objDevolucion.ActualizarCantidadPrestada(Integer.parseInt(String.valueOf(tblDetalleDevolucion.getValueAt(fila, 4))), String.valueOf(tblDetalleDevolucion.getValueAt(fila, 2)));
                
                objDevolucion.ActualizarExistenciaMax(cantaDevuelta, isbnx);
            }
            limpiarTabla();
        }
    }

