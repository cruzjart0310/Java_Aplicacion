    private void tblPrestamosPorUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPrestamosPorUsuariosMouseClicked
        // TODO add your handling code here:
        //Capturamos cada fila de la tabla
        try {
            int fila = this.tblPrestamosPorUsuarios.rowAtPoint(evt.getPoint());

            int cantidadExistente = Integer.parseInt(String.valueOf(this.tblPrestamosPorUsuarios.getValueAt(fila, 4)));
            String CantLibroPrestamos = "";
            int cantidad;

            if (cantidadExistente == 1 || cantidadExistente == 0) {
                int i = JOptionPane.showConfirmDialog(this, "Desea ejecutar la devolución de este ejemplar ?", "Msj.", JOptionPane.YES_NO_OPTION);
                if (i == 0) {

                    modelTablaDetalleDevolucion.addRow(new Object[]{
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 0),
                        txtCodigoDevolucion.getText(),
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 2),
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 3), 1});
                    tblDetalleDevolucion.setModel(modelTablaDetalleDevolucion);
                }
            } else {

                CantLibroPrestamos = JOptionPane.showInputDialog(null, "Introduce la cantidad de ejemplares a devolver:",
                        "Msj.", JOptionPane.INFORMATION_MESSAGE);

                while (CantLibroPrestamos != null && !validarDatos.esEntero(CantLibroPrestamos)) {
                    CantLibroPrestamos = JOptionPane.showInputDialog(null, "Introduce la cantidad de ejemplar a devolver:",
                            "Msj.", JOptionPane.INFORMATION_MESSAGE);
                }
                cantidad = Integer.parseInt(CantLibroPrestamos);

                if (cantidadExistente >= cantidad && cantidad > 0) {
                    modelTablaDetalleDevolucion.addRow(new Object[]{
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 0),
                        txtCodigoDevolucion.getText(),
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 2),
                        tblPrestamosPorUsuarios.getValueAt(tblPrestamosPorUsuarios.getSelectedRow(), 3), cantidad});
                    tblDetalleDevolucion.setModel(modelTablaDetalleDevolucion);

                } else {
                    JOptionPane.showMessageDialog(null, "No tiene suficiente ejemplares", "Msj.", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception ex) {
            ex.getMessage();
        }
    }//GEN-LAST:event_tblPrestamosPorUsuariosMouseClicked

