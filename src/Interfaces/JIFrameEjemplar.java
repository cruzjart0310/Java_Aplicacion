    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (objEjemplar.EliminarEjemplar(txtIbsn.getText()) == true) {
            JOptionPane.showMessageDialog(this, "Eliminación exitosa", "Msj.", JOptionPane.INFORMATION_MESSAGE);
            LimpiarCampos();
            JIFrameConsultaEjemplares.tblEjemplar.setModel(objEjemplar.ConsultaEjemplar());
        }
        dispose();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if (objEjemplar.ModificarEjemplar(txtClasificacion.getText(), txtTitulo.getText(), txtAutor.getText(), txtEditorial.getText(), Integer.parseInt(txtCantidad.getText()),txtIbsn.getText()) == true) {
        JOptionPane.showMessageDialog(this, "Cambios efectuados con éxito", "Msj.", JOptionPane.INFORMATION_MESSAGE);
        LimpiarCampos();
        JIFrameConsultaEjemplares.tblEjemplar.setModel(objEjemplar.ConsultaEjemplar());
        }
        dispose();
    }//GEN-LAST:event_btnActualizarActionPerformed

