    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtIbsn.getText().equals("") || txtClasificacion.getText().equals("") || txtTitulo.getText().equals("") || txtAutor.getText().equals("") || txtEditorial.getText().equals("") || txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Rellene todos los campos", "Msj.", JOptionPane.WARNING_MESSAGE);

        } else {
            if (objEjemplar.RegistroEjemplar(txtIbsn.getText(), txtClasificacion.getText(), txtTitulo.getText(), txtAutor.getText(), txtEditorial.getText(), Integer.parseInt(txtCantidad.getText()), Integer.parseInt(txtCantidad.getText()), 0) == true) {
                JOptionPane.showMessageDialog(this, "Registro exitoso", "Msj.", JOptionPane.INFORMATION_MESSAGE);
                JIFrameConsultaEjemplares.tblEjemplar.setModel(objEjemplar.ConsultaEjemplar());
            }
            LimpiarCampos();
        }
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

